const double PI = acos(-1.0);

double w, b, d, s;

void solve() {
  cin >> w >> b >> d >> s;
  int ans = 0;
  int t;
  cin >> t;
  while(t--) {
    double x, y;
    cin >> x >> y;
    point p(x, y);
    if(length(p) <= b) {
      ans += 50;
    } else if(length(p) <= s) {
      double alpha = angle(p);
      if(alpha < 0) alpha += 2 * PI;
      int cur = ceil(alpha / (2 * PI / w));
      if(length(p) <= d) {
        cur *= 2;
      }
      ans += cur;
    }
  }
  cout << ans << endl;
}

int main()
{
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int T;
  cin >> T;
  while(T--) {
    solve();
  }
  return 0;
}
